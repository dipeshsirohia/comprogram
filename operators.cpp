# include <iostream>
using namespace std;
int main(){
    // operators in c++
    int a=3,b=6;
    cout<<"sum of a+b= "<<a+b<<endl;
    cout<<"subtraction of a-b= "<<a-b<<endl;
    cout<<"multiplication of a*b= "<<a*b<<endl;
    cout<<"division of b/a= "<<b/a<<endl;
    cout<<"a++"<<a++<<endl;
    cout<<"a--"<<a--<<endl;
    cout<<"++a "<<++a<<endl;
    cout<<"--a "<<--a<<endl;
    cout<<endl;
    // comparison
    cout<<"equivalent  "<<(a==b)<<endl;
    cout<<"a<=b  "<<(a<=b)<<endl;
    cout<<"a>=b  "<<(a>=b)<<endl;
    cout<<"a<b  "<<(a<b)<<endl;
    cout<<"a>b  "<<(a>b)<<endl;

    cout<<endl;
    cout<<"equivalent  "<<(a==b)<<endl;
    cout<<"a<=b  "<<!(a<=b)<<endl;
    cout<<"a>=b  "<<!(a>=b)<<endl;
    cout<<"a<b  "<<!(a<b)<<endl;
    cout<<"a>b  "<<!(a>b)<<endl;
    cout<<endl;
    // logical operator
    cout<<"equivalent and grater than "<<((a==b)&&(a>b))<<endl;
    




    
}